    %repeat = 0;
    
    fid = fopen('CORDIC_LOG.txt', 'w');

    while (i < iterationsAct)
        if (u == 1) 
            idx = shiftsCirc(iterations/4, i);
        elseif (u == -1)
            idx = shiftsHyp(iterations/4, i);
        else
            idx = i-1;
        end
        %get sign aka d
        if (mode == 0)
            if (zReg >= 0)
                d = 1;
            else
                d = -1;
            end
        else
            if (((xReg >= 0) && (yReg >= 0)) || ((xReg < 0) && (yReg < 0)))
                d = -1;
            else
                d = 1;
            end
        end

        xRegN = xReg - u*d*2^(-(idx))*yReg;
        yRegN = yReg + d*2^(-(idx))*xReg;
        zRegN = zReg - d*lookUp(idx+1);

        xReg = xRegN;
        yReg = yRegN;
        zReg = zRegN;

        %if ((u == -1) && ((i == 4) || (i == 13) || (i == 40)) && (repeat == 0))
        %    repeat = 1;
        %else
            i = i + 1;
        %    repeat = 0;
        %end
        fprintf(fid, "-----------------------------\n");
        fprintf(fid, "  iteration: %d \n", iterations-idx);
        fprintf(fid, "-----------------------------\n");
        fprintf(fid, "xReg: %f \n", xReg);
        fprintf(fid, "yReg: %f \n", yReg);
        fprintf(fid, "zReg: %f \n", zReg);
        fprintf(fid, "shift: %f \n", idx);
        fprintf(fid, "d: %f \n", d);
        fprintf(fid, "-----------------------------\n\n");
    end
    if (u == 1)
            xO = xReg / 2;
            yO = yReg /2;
            if (mode == 0)
                zO = zReg;
            else
                zO = mod(zReg + 1e-9, 2*pi);
            end
            
    elseif (u == -1)
            xO = xReg*2;
            yO = yReg*2;
            zO = zReg;
